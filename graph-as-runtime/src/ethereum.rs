/// Read a full EthereumEvent from AS memory.
///
/// # Safety
///
/// `ptr` must be a valid AscPtr to an EthereumEvent object written by graph-node.
pub unsafe fn read_ethereum_event(ptr: u32) -> RawEthereumEvent {
    unsafe {
        // EthereumEvent payload field offsets (each is a u32 AscPtr):
        //   0  address
        //   4  logIndex
        //   8  transactionLogIndex
        //  12  logType
        //  16  block
        //  20  transaction
        //  24  parameters
        //  28  receipt

        let address_ptr = read_u32_at(ptr, 0);
        let log_index_ptr = read_u32_at(ptr, 4);
        let block_ptr = read_u32_at(ptr, 16);
        let tx_ptr = read_u32_at(ptr, 20);
        let params_ptr = read_u32_at(ptr, 24);

        // address: Uint8Array (20 bytes)
        let address = if address_ptr != 0 {
            read_fixed_bytes::<20>(address_ptr)
        } else {
            [0u8; 20]
        };

        // logIndex: BigInt (Uint8Array)
        let log_index = if log_index_ptr != 0 {
            read_uint8array(log_index_ptr)
        } else {
            vec![0u8]
        };

        // block.number and block.timestamp
        // EthereumBlock offsets:
        //   28  number    -> BigInt
        //   40  timestamp -> BigInt
        let (block_number, block_timestamp) = if block_ptr != 0 {
            let num_ptr = read_u32_at(block_ptr, 28);
            let ts_ptr = read_u32_at(block_ptr, 40);
            let num = if num_ptr != 0 {
                read_uint8array(num_ptr)
            } else {
                vec![0u8]
            };
            let ts = if ts_ptr != 0 {
                read_uint8array(ts_ptr)
            } else {
                vec![0u8]
            };
            (num, ts)
        } else {
            (vec![0u8], vec![0u8])
        };

        // transaction.hash: Bytes (32 bytes)
        // EthereumTransaction offsets:
        //   0  hash -> Bytes (32)
        let tx_hash = if tx_ptr != 0 {
            let hash_ptr = read_u32_at(tx_ptr, 0);
            if hash_ptr != 0 {
                read_fixed_bytes::<32>(hash_ptr)
            } else {
                [0u8; 32]
            }
        } else {
            [0u8; 32]
        };

        // parameters: Array<EthereumEventParam>
        let params = if params_ptr != 0 {
            read_event_params_array(params_ptr)
        } else {
            vec![]
        };

        RawEthereumEvent {
            address,
            log_index,
            block_number,
            block_timestamp,
            tx_hash,
            params,
        }
    }
}

/// Read a full `EthereumCall` from AS memory.
///
/// Called by `#[handler(call)]` WASM entry points with the `call_ptr`
/// argument passed by graph-node.
///
/// # Safety
///
/// `ptr` must be a valid AscPtr to an `EthereumCall` object written by graph-node.
///
/// # Memory layout
///
/// ```text
/// EthereumCall payload (u32 AscPtrs):
///   offset  0  address      -> Uint8Array (20 bytes)
///   offset  4  block        -> EthereumBlock
///   offset  8  transaction  -> EthereumTransaction
///   offset 12  inputValues  -> Array<EthereumEventParam>
///   offset 16  outputValues -> Array<EthereumEventParam>
/// ```
pub unsafe fn read_ethereum_call(ptr: u32) -> RawEthereumCall {
    unsafe {
        let address_ptr = read_u32_at(ptr, 0);
        let block_ptr = read_u32_at(ptr, 4);
        let tx_ptr = read_u32_at(ptr, 8);
        let inputs_ptr = read_u32_at(ptr, 12);
        let outputs_ptr = read_u32_at(ptr, 16);

        // address: Uint8Array (20 bytes)
        let address = if address_ptr != 0 {
            read_fixed_bytes::<20>(address_ptr)
        } else {
            [0u8; 20]
        };

        // block.number and block.timestamp (same offsets as in EthereumEvent)
        let (block_number, block_timestamp) = if block_ptr != 0 {
            let num_ptr = read_u32_at(block_ptr, 28);
            let ts_ptr = read_u32_at(block_ptr, 40);
            let num = if num_ptr != 0 { read_uint8array(num_ptr) } else { vec![0u8] };
            let ts = if ts_ptr != 0 { read_uint8array(ts_ptr) } else { vec![0u8] };
            (num, ts)
        } else {
            (vec![0u8], vec![0u8])
        };

        // transaction.hash (offset 0) and transaction.from (offset 8)
        let (tx_hash, from) = if tx_ptr != 0 {
            let hash_ptr = read_u32_at(tx_ptr, 0);
            let from_ptr = read_u32_at(tx_ptr, 8);
            let hash = if hash_ptr != 0 { read_fixed_bytes::<32>(hash_ptr) } else { [0u8; 32] };
            let from = if from_ptr != 0 { read_fixed_bytes::<20>(from_ptr) } else { [0u8; 20] };
            (hash, from)
        } else {
            ([0u8; 32], [0u8; 20])
        };

        // inputValues and outputValues: Array<EthereumEventParam>
        let inputs = if inputs_ptr != 0 { read_event_params_array(inputs_ptr) } else { vec![] };
        let outputs = if outputs_ptr != 0 { read_event_params_array(outputs_ptr) } else { vec![] };

        RawEthereumCall {
            address,
            block_number,
            block_timestamp,
            tx_hash,
            from,
            inputs,
            outputs,
        }
    }
}
