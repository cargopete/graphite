/// A decoded Ethereum event parameter value.
#[derive(Clone)]
pub enum EthereumValue {
    Address([u8; 20]),
    FixedBytes(Vec<u8>),
    Bytes(Vec<u8>),
    /// Little-endian two's-complement signed integer bytes (graph-ts BigInt).
    Int(Vec<u8>),
    /// Little-endian unsigned integer bytes (graph-ts BigInt).
    Uint(Vec<u8>),
    Bool(bool),
    String(String),
    /// Dynamic array: `T[]`
    Array(Vec<EthereumValue>),
    /// Fixed-size array: `T[N]`
    FixedArray(Vec<EthereumValue>),
    /// Struct/tuple: `(T1, T2, ...)`
    Tuple(Vec<EthereumValue>),
    Unknown(u32),
}

impl EthereumValue {
    pub fn as_uint(&self) -> Option<Vec<u8>> {
        if let EthereumValue::Uint(b) = self { Some(b.clone()) } else { None }
    }
    pub fn as_int(&self) -> Option<Vec<u8>> {
        if let EthereumValue::Int(b) = self { Some(b.clone()) } else { None }
    }
    pub fn as_address(&self) -> Option<[u8; 20]> {
        if let EthereumValue::Address(a) = self { Some(*a) } else { None }
    }
    pub fn as_bool(&self) -> Option<bool> {
        if let EthereumValue::Bool(b) = self { Some(*b) } else { None }
    }
    pub fn as_bytes(&self) -> Option<Vec<u8>> {
        match self {
            EthereumValue::Bytes(b) | EthereumValue::FixedBytes(b) => Some(b.clone()),
            _ => None,
        }
    }
    pub fn as_string(&self) -> Option<&str> {
        if let EthereumValue::String(s) = self { Some(s.as_str()) } else { None }
    }
    /// Returns the elements of an `Array` or `FixedArray` value.
    pub fn as_array(&self) -> Option<&Vec<EthereumValue>> {
        match self {
            EthereumValue::Array(v) | EthereumValue::FixedArray(v) => Some(v),
            _ => None,
        }
    }
    /// Returns the elements of a `Tuple` value.
    pub fn as_tuple(&self) -> Option<&Vec<EthereumValue>> {
        if let EthereumValue::Tuple(v) = self { Some(v) } else { None }
    }
}

/// A single named event parameter.
pub struct EventParam {
    pub name: String,
    pub value: EthereumValue,
}

/// Everything a handler needs from an EthereumEvent, in plain Rust types.
pub struct RawEthereumEvent {
    /// The contract address that emitted the event (20 bytes).
    pub address: [u8; 20],
    /// Log index (BigInt bytes, little-endian).
    pub log_index: Vec<u8>,
    /// Block number (BigInt bytes, little-endian).
    pub block_number: Vec<u8>,
    /// Block timestamp (BigInt bytes, little-endian).
    pub block_timestamp: Vec<u8>,
    /// Transaction hash (32 bytes).
    pub tx_hash: [u8; 32],
    /// Decoded event parameters in declaration order.
    pub params: Vec<EventParam>,
}

/// Everything a call handler needs from an `ethereum.Call`, in plain Rust types.
///
/// # Memory layout
///
/// The `EthereumCall` AS payload (u32 AscPtrs, in declaration order):
/// ```text
///   offset  0  address      -> Uint8Array (20 bytes)
///   offset  4  block        -> EthereumBlock
///   offset  8  transaction  -> EthereumTransaction
///   offset 12  inputValues  -> Array<EthereumEventParam>
///   offset 16  outputValues -> Array<EthereumEventParam>
/// ```
pub struct RawEthereumCall {
    /// The contract address that was called (20 bytes).
    pub address: [u8; 20],
    /// Block number (BigInt bytes, little-endian).
    pub block_number: Vec<u8>,
    /// Block timestamp (BigInt bytes, little-endian).
    pub block_timestamp: Vec<u8>,
    /// Transaction hash (32 bytes).
    pub tx_hash: [u8; 32],
    /// Transaction sender address (20 bytes) — from `transaction.from`.
    pub from: [u8; 20],
    /// Decoded call input parameters in declaration order.
    pub inputs: Vec<EventParam>,
    /// Decoded call output values in declaration order.
    pub outputs: Vec<EventParam>,
}

impl RawEthereumCall {
    /// Find a named input parameter by name.
    pub fn find_input(&self, name: &str) -> Option<&EthereumValue> {
        self.inputs.iter().find(|p| p.name == name).map(|p| &p.value)
    }

    /// Find a named output value by name (or `"value"` for single-return functions).
    pub fn find_output(&self, name: &str) -> Option<&EthereumValue> {
        self.outputs.iter().find(|p| p.name == name).map(|p| &p.value)
    }

    /// Find a named input parameter and return its uint bytes.
    pub fn find_input_uint(&self, name: &str) -> Result<Vec<u8>, &'static str> {
        match self.find_input(name) {
            Some(EthereumValue::Uint(b)) => Ok(b.clone()),
            _ => Err("input param not found or wrong type"),
        }
    }

    /// Find a named input parameter and return its address bytes.
    pub fn find_input_address(&self, name: &str) -> Result<[u8; 20], &'static str> {
        match self.find_input(name) {
            Some(EthereumValue::Address(a)) => Ok(*a),
            _ => Err("input param not found or wrong type"),
        }
    }

    /// Find a named input parameter and return its bool value.
    pub fn find_input_bool(&self, name: &str) -> Result<bool, &'static str> {
        match self.find_input(name) {
            Some(EthereumValue::Bool(b)) => Ok(*b),
            _ => Err("input param not found or wrong type"),
        }
    }

    /// Find a named input parameter and return its bytes.
    pub fn find_input_bytes(&self, name: &str) -> Result<Vec<u8>, &'static str> {
        match self.find_input(name) {
            Some(EthereumValue::Bytes(b)) | Some(EthereumValue::FixedBytes(b)) => Ok(b.clone()),
            _ => Err("input param not found or wrong type"),
        }
    }
}

