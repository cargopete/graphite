/// Read a `u32` from `base + offset` (unaligned-safe).
#[inline(always)]
unsafe fn read_u32_at(base: u32, offset: u32) -> u32 {
    let ptr = (base + offset) as *const u32;
    unsafe { ptr.read_unaligned() }
}

/// Read a `u64` from `base + offset` (unaligned-safe).
#[inline(always)]
unsafe fn read_u64_at(base: u32, offset: u32) -> u64 {
    let ptr = (base + offset) as *const u64;
    unsafe { ptr.read_unaligned() }
}

/// Read `len` bytes starting at absolute address `data_ptr`.
#[inline(always)]
unsafe fn read_bytes(data_ptr: u32, len: u32) -> Vec<u8> {
    if len == 0 {
        return vec![];
    }
    let ptr = data_ptr as *const u8;
    let slice = unsafe { core::slice::from_raw_parts(ptr, len as usize) };
    slice.to_vec()
}

