//! Ethereum event reader — decodes AS-ABI EthereumEvent objects from WASM memory.
//!
//! When graph-node calls a handler such as `handle_transfer(event_ptr: i32)`, the
//! `event_ptr` is an AscPtr pointing at an EthereumEvent object in linear memory.
//! This module reads that object and returns a plain Rust struct.
//!
//! # Safety
//!
//! All functions in this module read raw WASM linear memory via pointer arithmetic.
//! Callers must ensure the pointers originate from graph-node and have not been
//! tampered with.  In normal subgraph operation this is always the case.
//!
//! # Memory layout reference
//!
//! Each AS object is preceded by a 16-byte header; AscPtr values point *past* the
//! header into the payload.  All multi-byte values are little-endian (wasm32 is LE).
//!
//! ```text
//! EthereumEvent (payload, each field is a u32 AscPtr):
//!   offset  0  address              -> Uint8Array (20 bytes)
//!   offset  4  logIndex             -> BigInt (Uint8Array)
//!   offset  8  transactionLogIndex  -> BigInt (Uint8Array)
//!   offset 12  logType              -> AscString (nullable)
//!   offset 16  block                -> EthereumBlock
//!   offset 20  transaction          -> EthereumTransaction
//!   offset 24  parameters           -> Array<EthereumEventParam>
//!   offset 28  receipt              -> nullable
//!
//! EthereumEventParam:
//!   offset 0  name   -> AscString
//!   offset 4  value  -> EthereumValue (AscEnum)
//!
//! EthereumValue (AscEnum, 16-byte payload):
//!   offset 0  kind   u32
//!   offset 4  _pad   u32
//!   offset 8  data   u64  (AscPtr cast to u64 for pointer kinds)
//!
//! Uint8Array / BigInt / Bytes (12-byte payload):
//!   offset 0  buffer_ptr   u32  -> ArrayBuffer
//!   offset 4  data_start   u32  -> absolute ptr to start of data
//!   offset 8  byte_length  u32  -> number of bytes
//!
//! Array<T> (16-byte payload):
//!   offset  0  buffer_ptr         u32
//!   offset  4  buffer_data_start  u32  -> absolute ptr to first element
//!   offset  8  buffer_data_len    u32  -> total byte length of data
//!   offset 12  length             u32  -> element count
//!
//! AscString:
//!   The 16-byte header field rt_size holds the UTF-16LE byte count.
//!   The payload is the UTF-16LE code units directly (no null terminator).
//!   rt_size is at header offset 12 (i.e. ptr - 4).
//!
//! EthereumBlock (payload, u32 AscPtrs):
//!   offset  0  hash             -> Bytes (32)
//!   offset  4  parentHash       -> Bytes (32)
//!   offset  8  unclesHash       -> Bytes (32)
//!   offset 12  author           -> Address (20)
//!   offset 16  stateRoot        -> Bytes (32)
//!   offset 20  transactionsRoot -> Bytes (32)
//!   offset 24  receiptsRoot     -> Bytes (32)
//!   offset 28  number           -> BigInt
//!   offset 32  gasUsed          -> BigInt
//!   offset 36  gasLimit         -> BigInt
//!   offset 40  timestamp        -> BigInt
//!   offset 44  difficulty       -> BigInt
//!   offset 48  totalDifficulty  -> BigInt
//!   offset 52  size             -> BigInt (nullable)
//!   offset 56  baseFeePerGas    -> BigInt (nullable)
//!
//! EthereumTransaction (payload, u32 AscPtrs):
//!   offset  0  hash     -> Bytes (32)
//!   offset  4  index    -> BigInt
//!   offset  8  from     -> Address (20)
//!   offset 12  to       -> Address (nullable)
//!   offset 16  value    -> BigInt
//!   offset 20  gasLimit -> BigInt
//!   offset 24  gasPrice -> BigInt
//!   offset 28  input    -> Bytes
//! ```

use alloc::{string::String, vec, vec::Vec};

