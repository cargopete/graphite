/// Read a Uint8Array / Bytes / BigInt — returns the raw bytes.
///
/// The AS TypedArray payload is:
///   [buffer_ptr: u32][data_start: u32][byte_length: u32]
///
/// `data_start` is the absolute pointer into the ArrayBuffer payload.
unsafe fn read_uint8array(ptr: u32) -> Vec<u8> {
    unsafe {
        let data_start = read_u32_at(ptr, 4);
        let byte_length = read_u32_at(ptr, 8);
        read_bytes(data_start, byte_length)
    }
}

/// Read exactly `N` bytes from a Uint8Array, zero-padding if shorter.
unsafe fn read_fixed_bytes<const N: usize>(ptr: u32) -> [u8; N] {
    let bytes = unsafe { read_uint8array(ptr) };
    let mut out = [0u8; N];
    let copy_len = bytes.len().min(N);
    out[..copy_len].copy_from_slice(&bytes[..copy_len]);
    out
}

/// Read an AS String and decode UTF-16LE → Rust String.
///
/// The rt_size field in the header (at ptr - 4) gives the UTF-16LE byte count.
/// The payload starts at ptr and contains that many bytes of UTF-16LE.
unsafe fn read_asc_string(ptr: u32) -> String {
    if ptr == 0 {
        return String::new();
    }
    // rt_size is at header offset 12, i.e. *(ptr - 4)
    let byte_len = unsafe { read_u32_at(ptr - 4, 0) };
    if byte_len == 0 {
        return String::new();
    }
    let char_count = byte_len / 2;
    let raw_ptr = ptr as *const u16;
    let slice = unsafe { core::slice::from_raw_parts(raw_ptr, char_count as usize) };
    // Decode UTF-16LE; replace surrogates with U+FFFD.
    char::decode_utf16(slice.iter().copied())
        .map(|r| r.unwrap_or('\u{FFFD}'))
        .collect()
}

/// Read an `Array<AscPtr<EthereumValue>>` and return a `Vec<EthereumValue>`.
///
/// Used for array and tuple parameters. Layout is the same as
/// `Array<EthereumEventParam>`: element pointers are u32 values packed
/// at `buffer_data_start`.
unsafe fn read_ethereum_value_array(arr_ptr: u32) -> Vec<EthereumValue> {
    if arr_ptr == 0 {
        return vec![];
    }
    unsafe {
        let data_start = read_u32_at(arr_ptr, 4);
        let length = read_u32_at(arr_ptr, 12);
        let mut values = Vec::with_capacity(length as usize);
        for i in 0..length {
            let elem_ptr = read_u32_at(data_start, i * 4);
            values.push(if elem_ptr != 0 {
                read_ethereum_value(elem_ptr)
            } else {
                EthereumValue::Unknown(0)
            });
        }
        values
    }
}

/// Read an Array<EthereumEventParam> and return a Vec of EventParam.
///
/// Array<T> payload:
///   [buffer_ptr: u32][buffer_data_start: u32][buffer_data_len: u32][length: u32]
///
/// Each element is a u32 AscPtr to an EthereumEventParam.
unsafe fn read_event_params_array(arr_ptr: u32) -> Vec<EventParam> {
    if arr_ptr == 0 {
        return vec![];
    }
    unsafe {
        let data_start = read_u32_at(arr_ptr, 4);
        let length = read_u32_at(arr_ptr, 12);
        let mut params = Vec::with_capacity(length as usize);
        for i in 0..length {
            let elem_ptr = read_u32_at(data_start, i * 4);
            if elem_ptr == 0 {
                continue;
            }
            let param = read_event_param(elem_ptr);
            params.push(param);
        }
        params
    }
}

/// Read a single EthereumEventParam.
///
/// Layout:
///   offset 0  name   u32 AscPtr<AscString>
///   offset 4  value  u32 AscPtr<EthereumValue>
unsafe fn read_event_param(ptr: u32) -> EventParam {
    unsafe {
        let name_ptr = read_u32_at(ptr, 0);
        let value_ptr = read_u32_at(ptr, 4);
        let name = read_asc_string(name_ptr);
        let value = read_ethereum_value(value_ptr);
        EventParam { name, value }
    }
}

/// Read an EthereumValue (AscEnum).
///
/// AscEnum payload (16 bytes):
///   offset 0  kind   u32
///   offset 4  _pad   u32
///   offset 8  data   u64
///
/// For pointer-typed variants, data is a u32 AscPtr cast to u64.
unsafe fn read_ethereum_value(ptr: u32) -> EthereumValue {
    if ptr == 0 {
        return EthereumValue::Unknown(0xff);
    }
    unsafe {
        let kind = read_u32_at(ptr, 0);
        let data = read_u64_at(ptr, 8);
        let data_ptr = data as u32; // lower 32 bits used for AscPtr

        match kind {
            KIND_ADDRESS => {
                // data points to a Uint8Array of 20 bytes
                let bytes = read_fixed_bytes::<20>(data_ptr);
                EthereumValue::Address(bytes)
            }
            KIND_FIXED_BYTES => {
                let bytes = read_uint8array(data_ptr);
                EthereumValue::FixedBytes(bytes)
            }
            KIND_BYTES => {
                let bytes = read_uint8array(data_ptr);
                EthereumValue::Bytes(bytes)
            }
            KIND_INT => {
                // BigInt: Uint8Array in little-endian two's-complement
                let bytes = read_uint8array(data_ptr);
                EthereumValue::Int(bytes)
            }
            KIND_UINT => {
                let bytes = read_uint8array(data_ptr);
                EthereumValue::Uint(bytes)
            }
            KIND_BOOL => {
                // data is 0 or 1 stored as u64
                EthereumValue::Bool(data != 0)
            }
            KIND_STRING => {
                let s = read_asc_string(data_ptr);
                EthereumValue::String(s)
            }
            KIND_FIXED_ARRAY => {
                let items = read_ethereum_value_array(data_ptr);
                EthereumValue::FixedArray(items)
            }
            KIND_ARRAY => {
                let items = read_ethereum_value_array(data_ptr);
                EthereumValue::Array(items)
            }
            KIND_TUPLE => {
                let items = read_ethereum_value_array(data_ptr);
                EthereumValue::Tuple(items)
            }
            other => EthereumValue::Unknown(other),
        }
    }
}

