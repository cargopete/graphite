/// Trait implemented by generated event structs to decode from a
/// `RawEthereumEvent` read out of AS memory.
///
/// Implementations are generated by `graphite codegen` for each ABI event.
pub trait FromRawEvent: Sized {
    fn from_raw_event(raw: &RawEthereumEvent) -> Result<Self, &'static str>;
}

/// Trait implemented by generated call-input structs to decode from a
/// `RawEthereumCall` read out of AS memory.
///
/// Implementations are generated by `graphite codegen` for each ABI function
/// that has a corresponding call handler.
pub trait FromRawCall: Sized {
    fn from_raw_call(raw: &RawEthereumCall) -> Result<Self, &'static str>;
}

